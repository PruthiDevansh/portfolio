export type ThemeName = keyof typeof themes;

export function applyTheme(themeName: ThemeName) {
  const theme = themes[themeName];
  const root = document.documentElement;

  // Apply primary colors
  Object.entries(theme.primary).forEach(([shade, value]) => {
    root.style.setProperty(`--color-primary-${shade}`, value);
  });

  // Apply secondary colors
  Object.entries(theme.secondary).forEach(([shade, value]) => {
    root.style.setProperty(`--color-secondary-${shade}`, value);
  });

  // Apply accent colors
  Object.entries(theme.accent).forEach(([shade, value]) => {
    root.style.setProperty(`--color-accent-${shade}`, value);
  });

  // Store theme preference
  localStorage.setItem('portfolio-theme', themeName);
}

export function getStoredTheme(): ThemeName {
  const stored = localStorage.getItem('portfolio-theme') as ThemeName;
  return stored && themes[stored] ? stored : 'blue';
}

export function initializeTheme() {
  const theme = getStoredTheme();
  applyTheme(theme);
  return theme;
}
